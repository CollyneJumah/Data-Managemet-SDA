public void FilterChr(){
        int component;
        Connection con=null;
        ResultSet rs;
        Statement st;
        PreparedStatement pst=null;

        try{
            String dbname="sda";
            String pass="";
            String username="root";
            String Url="jdbc:mysql://localhost:3306/";
            String Drivers="com.mysql.jdbc.Driver";

            Class.forName(Drivers);
            con=DriverManager.getConnection(Url+dbname,username,pass);
            String sql="SELECT * FROM registermembers WHERE regNumber LIKE '%"+searchMemberChoir.getText()+"%'|| Name LIKE '%"+searchMemberChoir.getText()+"%'|| phone LIKE '%"+searchMemberChoir.getText()+"%'";
            st=con.createStatement();
            rs=st.executeQuery(sql); 
            while (rs.next()==true){
                String n=rs.getString("Name");
                String rn=rs.getString("regNumber");
                String gd=rs.getString("Gender");
                String phn=rs.getString("phone");
                String yos=rs.getString("Year");
                txtNameChr.setText(n);
                txtRegChr.setText(rn);
                txtPhoneChr.setText(phn);
                txtYearChr.setText(yos);
                cboGenderChr.setSelectedItem(gd);
        }
       
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,"NO RECORDS FOUND"+e.getLocalizedMessage());
        }
     }
